#Q.18 Ler o valor do raio de uma circunferência, calcular e escrever seu comprimento

#entrada
raio = float(input("Digite o valor do Raio da circunferencia: "))

#processamento
comprimento = 2 * (3.14 * raio)

#saida
print("O valor do raio da circunferencia {0:.2f} e o comprimento da circunferencia é {1:.2f}".format(raio, comprimento))